void send_mstp_trap(u32 event_mask)
{
    int event = 1;
    if (event_mask & (event << (int) MSTP_TRAP_NEW_ROOT)) {
        snmp_new_root_notification();
    }
    if (event_mask & (event << (int) MSTP_TRAP_TOPOLOGY_CHANGE)) {
        snmp_topology_change_notification();
    }
}

