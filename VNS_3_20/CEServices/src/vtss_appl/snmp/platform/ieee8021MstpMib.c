        *write_method = write_ieee8021MstpConfigurationName;
        memset (string, 0, sizeof (string));
        memcpy (string, table_entry.ieee8021MstpConfigurationName,
                sizeof (table_entry.ieee8021MstpConfigurationName));
        *var_len = MSTP_CONFIG_NAME_MAXLEN;
        return (u8 *) string;
